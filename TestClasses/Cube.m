@implementation Cube

- (id) init : (NSString*) name : (GLKVector3) pos : (GLKVector3) rot : (GLKVector3) scale : (RenderClass*) renderer : (NSMutableArray*) array {
    self = [super init];
    if (self) {
        _ID = name;
        _pos = pos;
        _rot = rot;
        _scale = scale;
        [super initializeModelView];
        if ((_renderer = renderer) != nil) {
            [_renderer setVertices:gCubeVertexData];
            [_renderer setSizeOfVertices:sizeof(gCubeVertexData)];
            [_renderer setIndicesNum:(sizeof(gCubeVertexData) / sizeof(GLfloat)) / 6];
        }
        _childGameObjects = array;
        
    }
    return self;
}

@end
